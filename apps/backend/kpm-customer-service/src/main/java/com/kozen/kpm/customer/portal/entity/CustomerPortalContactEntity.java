package com.kozen.kpm.customer.portal.entity;

public class CustomerPortalContactEntity {
    private String contactId;
    private String customerId;
    private String customerName;
    private String customerShortName;
    private String contactName;
    private String email;

    public String getContactId() { return contactId; }
    public void setContactId(String contactId) { this.contactId = contactId; }
    public String getCustomerId() { return customerId; }
    public void setCustomerId(String customerId) { this.customerId = customerId; }
    public String getCustomerName() { return customerName; }
    public void setCustomerName(String customerName) { this.customerName = customerName; }
    public String getCustomerShortName() { return customerShortName; }
    public void setCustomerShortName(String customerShortName) { this.customerShortName = customerShortName; }
    public String getContactName() { return contactName; }
    public void setContactName(String contactName) { this.contactName = contactName; }
    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }
}
